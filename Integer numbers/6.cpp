/*
	Сумма цифр трехзначного числа

	Дано целое трехзначное число. Найдите сумму его цифр.

	Формат входных данных

	На вход дается число от 100 до 999.

	Формат выходных данных

	Выведите одно целое число - ответ на задачу.

	Sample Input:
	476
	Sample Output:
	17
*/

#include <iostream>

using namespace std;

int main()
{
	int N, M, K, L;
	cin >> N;
	M = N % 10;
	L = N / 100;
	K = (N / 10) % 10;
	cout << L + M + K;
	return 0;
}